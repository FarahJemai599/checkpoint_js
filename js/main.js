var ta=document.getElementById("text");
function bold(){
    ta.style.fontWeight="bold";
}
function italic(){
    ta.style.fontStyle="italic";
}
function underline(){
    ta.style.textDecoration="underline";
}
function px(){
    ta.style.fontSize=document.getElementById("px").value;
}
function police(){
    ta.style.fontFamily=document.getElementById("police").value;
}
